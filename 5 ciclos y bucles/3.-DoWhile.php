<?php
// =====================================
//  BUCLE DO-WHILE
// =====================================
echo "\n Bucle DO-WHILE<br>";
$contador = 1;
do {
    echo "Iteración $contador<br>";
    $contador++;
} while ($contador <= 3);

/**
 * Se ejecuta AL MENOS una vez.
 * La condición se evalúa después del primer ciclo.
 */
?>